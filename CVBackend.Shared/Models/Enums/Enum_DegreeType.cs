    /// </summary>
    HighSchool = 0,

    /// <summary>
    /// Associate degree (typically 2 years).
    /// </summary>
    Associate = 1,

    /// <summary>
    /// Bachelor's degree (typically 4 years).
    /// </summary>
    Bachelor = 2,

    /// <summary>
    /// Master's degree (graduate level).
    /// </summary>
    Master = 3,

    /// <summary>
    /// Doctorate or PhD (highest academic degree).
    /// </summary>
    Doctorate = 4,

    /// <summary>
    /// Professional certificate or certification.
    /// </summary>
    Certificate = 5,

    /// <summary>
    /// Diploma program.
    /// </summary>
    Diploma = 6,

    /// <summary>
    /// Professional qualification or license.
    /// </summary>
    Professional = 7,

    /// <summary>
    /// Coding bootcamp or intensive training program.
    /// </summary>
    Bootcamp = 8,

    /// <summary>
    /// Online course or MOOC completion.
    /// </summary>
    OnlineCourse = 9
}
